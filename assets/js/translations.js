// Translation function
function t(key, lang = null) {
  const currentLang = lang || getCurrentLanguage();
  return translations[currentLang] && translations[currentLang][key] 
    ? translations[currentLang][key] 
    : key;
}

// Get current language from localStorage or default to Turkish
function getCurrentLanguage() {
  return localStorage.getItem('language') || 'tr';
}

// Set language
function setLanguage(lang) {
  localStorage.setItem('language', lang);
  updatePageContent();
  updateLanguageButton();
}

// Update all page content with current language
function updatePageContent() {
  const elements = document.querySelectorAll('[data-key]');
  elements.forEach(element => {
    const key = element.getAttribute('data-key');
    const translatedText = t(key);
    
    // Only update text if a translation exists; otherwise keep original content
    const hasTranslation = translatedText !== key;

    if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
      if (hasTranslation) element.placeholder = translatedText;
    } else {
      if (hasTranslation) element.textContent = translatedText;
    }
  });
}

// Update language button text
function updateLanguageButton() {
  const currentLang = getCurrentLanguage();
  const langButton = document.getElementById('current-lang');
  if (langButton) {
    langButton.textContent = currentLang.toUpperCase();
  }
  
  // Update html lang attribute
  document.documentElement.lang = currentLang;
}

// Switch language function
function switchLanguage() {
  const currentLang = getCurrentLanguage();
  const newLang = currentLang === 'tr' ? 'en' : 'tr';
  setLanguage(newLang);
}

// Initialize translations when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
  updatePageContent();
  updateLanguageButton();
});

// Export for use in other files
window.translations = {
  t,
  getCurrentLanguage,
  setLanguage,
  switchLanguage,
  updatePageContent,
  updateLanguageButton
};